    <h2>¿Qué es una función periódica?</h2>

<p style="font-size: larger;text-align:justify">Las funciones periódicas son aquellas funciones matemáticas cuyos valores se repiten a intervalos regulares, conocidos como periodos.</p>

   <p style="font-size: larger;text-align:justify"> Las funciones trigonométricas, como el seno y el coseno, son ejemplos clásicos de funciones periódicas. Estas funciones son esenciales para modelar y estudiar fenómenos que exhiben regularidad en el tiempo o en el espacio, como las ondas, los ciclos biológicos, los patrones climáticos y más.
    
    <br>Entender las funciones periódicas es crucial en campos como la física, la ingeniería y la economía, ya que permiten predecir comportamientos y diseñar sistemas que operan con ciclos regulares.</div>

