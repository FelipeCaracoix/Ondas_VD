    <h2>Amplitud</h2>

<p>La amplitud de una onda es una medida fundamental que describe la magnitud máxima de una oscilación en un punto particular de la onda. En términos más simples, es la distancia vertical desde el punto medio de la onda hasta la cresta (el punto más alto) o hasta el valle (el punto más bajo).

    Para visualizarlo, imagina que estás mirando las olas del mar. La altura de la ola desde su punto más tranquilo hasta la cima es la amplitud. En el contexto de las ondas sonoras, la amplitud está relacionada con el volumen del sonido: una amplitud mayor significa un sonido más fuerte.</p>
