    <h2>Frecuencia</h2>

<p style="font-size: larger;text-align:justify">La frecuencia de una onda se define como el número de ciclos completos que ocurren en un segundo, y se mide en Hertz (Hz). En términos matemáticos, es la inversa del período (T), que es el tiempo que tarda en completarse un ciclo. 
</p>

<p  style="font-size: larger;text-align:justify" >Al ajustar la frecuencia con el control deslizante, estás modificando la rapidez con la que la <b style="color:green">onda</b> se repite a lo largo del tiempo. 
</p>

<p style="font-size: larger;text-align:justify">La frecuencia de la función es de <b style="color:green">{Frecuencia}</b>.</p>

<p style="font-size: larger;text-align:justify">Un valor más alto en el rango significa más ciclos por segundo y una onda más “apretada”, mientras que un valor más bajo indica menos ciclos y una onda más “extendida”.</div>

